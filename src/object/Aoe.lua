function Aoe:init(x,y,opts)
	GameObject.init(self,x,y,opts)
end

function Aoe:update(dt)

end

function Aoe:draw()
	--local type = self.type
	self:drawAoe(self.x,self.y,"square33")
end

function Aoe:drawAoe(ox,oy,aoe)
	local ox = ox or self.x or 0
	local oy = oy or self.x or 0
    love.graphics.setColor(255,255,255)
    love.graphics.circle("fill",ox,oy,16)
    love.graphics.setColor(255,0,0)
    local aoe = self.type[aoe]
    for i, v in ipairs( aoe ) do
        love.graphics.circle("fill",ox + v[1]*32,oy + v[2]*32,16)
    end
	love.graphics.setColor(1,1,1,1)
end

function Aoe:keypressed(key,isrepeat,ox,oy)
    if key == "right" then
        ox = (ox + 32) % 800
    elseif key == "left" then
        ox = (ox - 32) % 800
    end
    if key == "up" then
        oy = (oy - 32) % 600
    elseif key == "down" then
        oy = (oy + 32) % 600
    end
    return ox,oy
end


return Aoe